package Familia;

public class Familia {
protected String familia, patriarca, tioabuelo;

public Familia(String familia, String patriarca, String tioabuelo) {
	this.familia=familia;
	this.patriarca=patriarca;
	this.tioabuelo=tioabuelo;

}

public Familia() {
	familia="Martínez";
	patriarca="Alfredo Martínez";
	tioabuelo="Armando Sánchez";
}


}
